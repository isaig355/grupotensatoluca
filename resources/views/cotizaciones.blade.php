$('#adicionar').click(function() {
  var tipo_material = document.getElementById("tipo_material").value;
  var material = document.getElementById("material").value;
  var tamaño = document.getElementById("tamaño").value;
  var cantidad = document.getElementById("cantidad").value;

  if(tipo_material=="Desechable"){
  	var r1 ="50";
  }

  if (tipo_material=="Retornable") {
  	var r1 ="100";
  }

  if (material=="Plastico") {
  	var r2 ="50";
  }

  if (material=="Cartón") {
  	var r2 ="40";
  }

  if (tamaño=="2.5 - 3 mts") {
  	var r3 ="300";
  }

  if (tamaño=="1.5 - 2.5 mts") {
  	var r3 ="200";
  }

  if (tamaño=="500 - 1.5 mts") {
  	var r3 ="100";
  }

  if (tamaño=="0 - 500 cm") {
  	var r3 ="50";
  }

  var r4 = cantidad;


  var suma_t = parseInt(r1)+parseInt(r2)+parseInt(r3);
  var mul_t = suma_t*r4*16/100;
  var iva = suma_t+mul_t;

  var i = 1; //contador para asignar id al boton que borrara la fila
  var fila = '<tr id="row' + i + '"><td id="tipo_material1">' + tipo_material + '</td><td>' + material + '</td><td>' + tamaño + '</td><td>' + cantidad + '</td><td>' + suma_t + '</td><td>' + iva + '</td><td><button type="button" name="remove" id="' + i + '" class="btn btn-danger btn_remove">Quitar</button></td></tr>'; //esto seria lo que contendria la fila

  i++;

  $('#mytable tr:first').after(fila);
    $("#adicionados").text(""); //esta instruccion limpia el div adicioandos para que no se vayan acumulando
    var nFilas = $("#mytable tr").length;
    $("#adicionados").append(nFilas - 1);
    //le resto 1 para no contar la fila del header
    document.getElementById("material").value ="";
    document.getElementById("tamaño").value = "";
    document.getElementById("cantidad").value ="";
    document.getElementById("tipo_material").value = "";
    document.getElementById("tipo_material").focus();
  });
$(document).on('click', '.btn_remove', function() {
  var button_id = $(this).attr("id");
    //cuando da click obtenemos el id del boton
    $('#row' + button_id + '').remove(); //borra la fila
    //limpia el para que vuelva a contar las filas de la tabla
    $("#adicionados").text("");
    var nFilas = $("#mytable tr").length;
    $("#adicionados").append(nFilas - 1);
  });
});
